public class Animal {
    private String Ten;
    private int Hinhanh;

    public Animal(String ten, int hinhanh) {
        Ten = ten;
        Hinhanh = hinhanh;
    }

    public String getTen() {
        return Ten;
    }

    public void setTen(String ten) {
        Ten = ten;
    }

    public int getHinhanh() {
        return Hinhanh;
    }

    public void setHinhanh(int hinhanh) {
        Hinhanh = hinhanh;
    }
}
